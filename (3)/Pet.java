
public class Pet {

	//Fields
	private String name;
	private String type;
	private int age;
	
	//Constructor
	public Pet() {
		
	}

	//Properties
	public String getName() {
		return name;
	}

	public void setName(String Name) {
		this.name = Name;
	}

	public String getType() {
		return type;
	}

	public void setType(String Type) {
		this.type = Type;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int Age) {
		this.age = Age;
	}	

}
